async function cleanupTestData() {
  // Clean up in correct order due to foreign key constraints
  const tables = [
    'moderation_logs',
    'appeals',
    'moderator_actions',
    'content_flags',
    'user_flags',
    'trust_scores',
    'reports',
    'moderation_cases',
    'messages',
    'conversation_members',
    'conversations',
    'bookings',
    'slots',
    'mentorships',
    'collaboration_members',
    'collaborations',
    'event_attendees',
    'events',
    'followers',
    'likes',
    'comments',
    'post_tags',
    'tags',
    'posts',
    'users',
    'roles'
  ];

  for (const table of tables) {
    try {
      await prisma.$executeRawUnsafe(`DELETE FROM ${table}`);
    } catch (error) {
      // Table might not exist or be empty, continue
    }
  }

  // Reset sequences
  try {
    await prisma.$executeRawUnsafe(`
      SELECT setval(pg_get_serial_sequence('reports', 'id'), 1, false);
      SELECT setval(pg_get_serial_sequence('moderation_cases', 'id'), 1, false);
      SELECT setval(pg_get_serial_sequence('moderator_actions', 'id'), 1, false);
      SELECT setval(pg_get_serial_sequence('trust_scores', 'id'), 1, false);
      SELECT setval(pg_get_serial_sequence('appeals', 'id'), 1, false);
      SELECT setval(pg_get_serial_sequence('moderation_logs', 'id'), 1, false);
      SELECT setval(pg_get_serial_sequence('users', 'id'), 1, false);
      SELECT setval(pg_get_serial_sequence('posts', 'id'), 1, false);
      SELECT setval(pg_get_serial_sequence('comments', 'id'), 1, false);
    `);
  } catch (error) {
    // Ignore sequence reset errors
  }
}

