export const testSqlInjection = async (endpoint, data, authToken) => {
  const sqlInjectionPayloads = [
    "'; DROP TABLE users; --",
    "' OR '1'='1",
    "'; SELECT * FROM users; --",
    "' UNION SELECT * FROM users --"
  ];

  const results = [];
  
  for (const payload of sqlInjectionPayloads) {
    const testData = { ...data, reason: payload };
    
    const response = await request(app)
      .post(endpoint)
      .set('Authorization', `Bearer ${authToken}`)
      .send(testData);
    
    results.push({
      payload,
      statusCode: response.statusCode,
      shouldNotCrash: response.statusCode < 500
    });
  }
  
  return results;
};

export const testXssPrevention = async (endpoint, data, authToken) => {
  const xssPayloads = [
    '<script>alert("xss")</script>',
    'javascript:alert("xss")',
    '<img src="x" onerror="alert(\'xss\')">',
    '"><script>alert("xss")</script>'
  ];

  const results = [];
  
  for (const payload of xssPayloads) {
    const testData = { ...data, description: payload };
    
    const response = await request(app)
      .post(endpoint)
      .set('Authorization', `Bearer ${authToken}`)
      .send(testData);
    
    const isSanitized = response.statusCode === 201 && 
                       !response.body.description?.includes('<script>');
    
    results.push({
      payload,
      statusCode: response.statusCode,
      isSanitized
    });
  }
  
  return results;
};

